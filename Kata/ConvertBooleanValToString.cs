using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Kata.BoolToWord
{
public class SolutionClass
    {
        public static string BoolToWord(bool word)
        {
            //if true return "Yes"
            //if false return "No"
            return word ? "Yes" : "No";
        }
    }
}
